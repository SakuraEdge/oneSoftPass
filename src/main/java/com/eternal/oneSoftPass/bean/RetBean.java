package com.eternal.oneSoftPass.bean;

public class RetBean {
    private String name;
    private String hui_in;
    private String hui_out;
    private String chao_in;
    private String chao_out;
    private String code;
    private String zhesuan;
    private String day;
    private String time;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHui_in() {
        return hui_in;
    }

    public void setHui_in(String hui_in) {
        this.hui_in = hui_in;
    }

    public String getHui_out() {
        return hui_out;
    }

    public void setHui_out(String hui_out) {
        this.hui_out = hui_out;
    }

    public String getChao_in() {
        return chao_in;
    }

    public void setChao_in(String chao_in) {
        this.chao_in = chao_in;
    }

    public String getChao_out() {
        return chao_out;
    }

    public void setChao_out(String chao_out) {
        this.chao_out = chao_out;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getZhesuan() {
        return zhesuan;
    }

    public void setZhesuan(String zhesuan) {
        this.zhesuan = zhesuan;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
